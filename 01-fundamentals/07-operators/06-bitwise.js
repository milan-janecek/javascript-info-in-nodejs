'use strict';

// The list of operators:
//
// AND ( & )
// OR ( | )
// XOR ( ^ )
// NOT ( ~ )
// LEFT SHIFT ( << )
// RIGHT SHIFT ( >> )
// ZERO-FILL RIGHT SHIFT ( >>> )

console.info(0 & 2);

console.info(0 | 2);

console.info(0 ^ 2);

console.info(~0);

console.info(2 << 1);

console.info(2 >> 1);

console.info(4 >>> 1);
